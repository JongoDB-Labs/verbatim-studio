def iter_terms() -> Iterable[RawTerm]:
    seen: set[str] = set()
    for canonical, gloss, subcat in _TERMS:
        key = canonical.lower()
        if key in seen:
            continue
        seen.add(key)
        # Industry-standard terms get a high baseline.
        score = 0.7
        yield RawTerm(
            term=canonical,
            canonical_form=canonical,
            category="tech",
            subcategory=subcat,
            context_blurb=gloss[:140],
            popularity_score=score,
            source="Curated tech terms",
        )
    logger.info("Tech terms (curated): %d yielded", len(seen))


